def generate_dataset(output_dir: Path) -> list[dict]:
    """Generate all 110 synthetic benchmark documents.

    Args:
        output_dir: Root output directory (typically DATASET_DIR).

    Returns:
        List of document metadata dicts.
    """
    documents: list[dict] = []

    for lang_key, lang_cfg in LANGUAGES.items():
        lang_content = CONTENT.get(lang_key, {})
        if not lang_content:
            print(f"WARNING: No content defined for '{lang_key}' — skipping.")
            continue

        font_path = _ensure_font(lang_cfg)
        if font_path is None:
            print(f"WARNING: No font available for '{lang_key}' — skipping.")
            continue

        lang_dir = output_dir / lang_key
        lang_dir.mkdir(parents=True, exist_ok=True)

        for idx, doc_type in enumerate(DOC_TYPES, start=1):
            lines = lang_content.get(doc_type)
            if not lines:
                print(f"  WARNING: Missing content for {lang_key}/{doc_type} — skipping.")
                continue

            file_stem = f"{doc_type}_{idx:03d}"
            img_path = lang_dir / f"{file_stem}.png"
            txt_path = lang_dir / f"{file_stem}.txt"

            print(f"Generating {lang_key}/{file_stem}.png...")

            img, words = _generate_image(lines, font_path, doc_type, lang_cfg["display"])
            img.save(str(img_path), "PNG")

            ground_truth = " ".join(words)
            txt_path.write_text(ground_truth, encoding="utf-8")

            documents.append({
                "doc_id": f"{lang_key}_{doc_type}_{idx:03d}",
                "language": lang_key,
                "script": lang_cfg["script"],
                "document_type": doc_type,
                "image_path": f"dataset/{lang_key}/{file_stem}.png",
                "ground_truth_path": f"dataset/{lang_key}/{file_stem}.txt",
                "word_count": len(words),
                "source": "synthetic",
                "license": "CC0",
            })

    return documents


