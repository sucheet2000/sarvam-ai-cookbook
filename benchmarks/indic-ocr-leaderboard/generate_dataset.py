def _load_pil_font(font_path: Path, size: int):
    """Load an ImageFont, falling back to the default bitmap font."""
    try:
        from PIL import ImageFont
        return ImageFont.truetype(str(font_path), size)
    except Exception:
        from PIL import ImageFont
        return ImageFont.load_default()


def _generate_image(
    content_lines: list[str],
    font_path: Path,
    doc_type: str,
    lang_display: str,
) -> tuple:
    """Render content lines onto a white A4-ish canvas.

    Args:
        content_lines: Text lines to render.
        font_path: Path to the .ttf font file.
        doc_type: Document type string (affects visual style).
        lang_display: Human-readable language name for header.

    Returns:
        Tuple of (PIL.Image, list[str]) where the list is all words.
    """
    from PIL import Image, ImageDraw

    IMG_W, IMG_H = 800, 1050
    MARGIN = 40
    HEADER_H = 70
    BG_COLOUR = (255, 255, 255)
    HEADER_COLOUR = (45, 65, 110)
    HEADER_TEXT_COLOUR = (255, 255, 255)
    BODY_TEXT_COLOUR = (30, 30, 30)
    LINE_COLOUR = (180, 180, 180)

    # Handwritten note gets a slightly off-white background
    if doc_type == "handwritten_note":
        BG_COLOUR = (255, 252, 235)

    img = Image.new("RGB", (IMG_W, IMG_H), BG_COLOUR)
    draw = ImageDraw.Draw(img)

    # Header bar
    draw.rectangle([(0, 0), (IMG_W, HEADER_H)], fill=HEADER_COLOUR)

    header_font = _load_pil_font(font_path, 22)
    header_label = f"{lang_display}  —  {doc_type.replace('_', ' ').title()}"
    draw.text((MARGIN, 20), header_label, font=header_font, fill=HEADER_TEXT_COLOUR)

    # Separator line
    draw.line([(0, HEADER_H), (IMG_W, HEADER_H)], fill=LINE_COLOUR, width=2)

    # Choose body font sizes based on doc type
    if doc_type == "newspaper_headline":
        sizes = [32, 24, 20, 18, 16]
    elif doc_type == "handwritten_note":
        sizes = [16] * 20
    else:
        sizes = [20, 16, 14, 14, 14, 14, 14, 14, 14, 14, 14, 14, 14, 14]

    y_cursor = HEADER_H + 24
    all_words: list[str] = []

    for i, line in enumerate(content_lines):
        if not line.strip():
            y_cursor += 10
            continue

        size = sizes[min(i, len(sizes) - 1)]
        font = _load_pil_font(font_path, size)

        # Wrap long lines
        max_chars = max(20, int((IMG_W - 2 * MARGIN) / (size * 0.55)))
        wrapped = textwrap.wrap(line, width=max_chars) or [line]

        for wline in wrapped:
            if y_cursor + size + 8 > IMG_H - MARGIN:
                break
            draw.text((MARGIN, y_cursor), wline, font=font, fill=BODY_TEXT_COLOUR)
            y_cursor += size + 8
            all_words.extend(wline.split())

        # Extra spacing after first two lines (heading area)
        if i < 2:
            y_cursor += 6

    # Bottom border line
    draw.line([(0, IMG_H - 30), (IMG_W, IMG_H - 30)], fill=LINE_COLOUR, width=1)

    return img, all_words


