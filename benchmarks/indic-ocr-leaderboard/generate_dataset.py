def _find_system_font() -> Optional[Path]:
    """Search common system font directories for any .ttf file."""
    candidates = [
        Path("/usr/share/fonts"),
        Path("/System/Library/Fonts"),
        Path("/Library/Fonts"),
        Path.home() / "Library" / "Fonts",
        Path("/usr/local/share/fonts"),
        Path("/Windows/Fonts"),
    ]
    for directory in candidates:
        if directory.exists():
            ttfs = list(directory.rglob("*.ttf"))
            if ttfs:
                return ttfs[0]
    return None


def _ensure_font(lang_cfg: dict) -> Optional[Path]:
    """Return path to a suitable font, downloading if necessary.

    Args:
        lang_cfg: Entry from LANGUAGES dict with font_file and font_url keys.

    Returns:
        Path to a .ttf file, or None if no font could be obtained.
    """
    FONTS_DIR.mkdir(parents=True, exist_ok=True)
    dest = FONTS_DIR / lang_cfg["font_file"]
    if dest.exists():
        return dest

    # Try downloading from Google Fonts CDN
    try:
        print(f"  Downloading {lang_cfg['font_file']}...")
        urllib.request.urlretrieve(lang_cfg["font_url"], dest)
        if dest.stat().st_size > 1000:
            return dest
        dest.unlink(missing_ok=True)
    except Exception as exc:
        print(f"  WARNING: download failed ({exc})")
        if dest.exists():
            dest.unlink(missing_ok=True)

    # Fall back to any available system font
    system_font = _find_system_font()
    if system_font:
        print(f"  WARNING: using system fallback font {system_font.name}")
        return system_font

    return None


